var angka = parseInt(prompt('Masukan Nilai'));

switch ( angka ) {
    case 1 :
        alert(' Angka Ynag Anda Masukan Adalah Angka 1');
        break;

    case 2 :
        alert(' Angka Yang Di Masukan Adalah Angka 2');
        break;

    case 3 :
        alert(' Angka Yang Di Masukan Adalah Angka 3');
        break;

    default:
        alert('Angka Yang ANda Masukan Salah');
        break;   
}